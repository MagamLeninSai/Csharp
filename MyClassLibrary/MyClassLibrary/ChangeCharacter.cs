using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyClassLibrary
{
    public class ChangeCharacter
    {
        public string UpperCase(String text)
        {
            return text.ToUpper();
        }
        public string LowerCase(String text)
        {
            return text.ToLower();
        }
    }
}
